package ma.mobility.abrid.realtime;

/** Niveau de gravité d'une perturbation. */
public enum DisruptionSeverity {
    /** Perturbation mineure — légère gêne prévisible. */
    LOW,
    /** Perturbation modérée — retards significatifs. */
    MEDIUM,
    /** Perturbation majeure — trafic fortement perturbé. */
    HIGH
}
